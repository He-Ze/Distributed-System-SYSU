
package mil.navy.nrl.norm.enums;

/**
 * Enumeration of the different types on NORM sync policies. This enumeration
 * maps to the native implementation and therefore is tied to the specific
 * version of NORM.
 *
 * @author Jason Rush
 */
 
public enum NormSyncPolicy {
  NORM_SYNC_CURRENT,
  NORM_SYNC_STREAM,
  NORM_SYNC_ALL;
}
