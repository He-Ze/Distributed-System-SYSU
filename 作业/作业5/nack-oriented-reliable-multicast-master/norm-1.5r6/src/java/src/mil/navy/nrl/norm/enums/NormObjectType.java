package mil.navy.nrl.norm.enums;

/**
 * Enumeration of the different types on NORM objects. This enumeration maps to
 * the native implementation and therefore is tied to the specific version of
 * NORM.
 * 
 * @author Jason Rush
 */
public enum NormObjectType {
  NORM_OBJECT_NONE,
  NORM_OBJECT_DATA,
  NORM_OBJECT_FILE,
  NORM_OBJECT_STREAM;
}
