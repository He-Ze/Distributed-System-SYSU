package mil.navy.nrl.norm.enums;

/**
 * Enumeration of the different types on NORM flush modes. This enumeration maps
 * to the native implementation and therefore is tied to the specific version of
 * NORM.
 * 
 * @author Jason Rush
 */
public enum NormFlushMode {
  NORM_FLUSH_NONE,
  NORM_FLUSH_PASSIVE,
  NORM_FLUSH_ACTIVE;
}
