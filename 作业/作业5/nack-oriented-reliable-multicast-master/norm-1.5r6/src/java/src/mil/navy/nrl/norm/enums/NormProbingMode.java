package mil.navy.nrl.norm.enums;

/**
 * Enumeration of the different types on NORM GRTT probing modes. This
 * enumeration maps to the native implementation and therefore is tied to the
 * specific version of NORM.
 * 
 * @author Jason Rush
 */
public enum NormProbingMode {
  NORM_PROBE_NONE,
  NORM_PROBE_PASSIVE,
  NORM_PROBE_ACTIVE;
}
